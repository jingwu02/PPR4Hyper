Y=[y([t_1,t_1,t_2]);y([t_2,t_3,t_3])].';
%Z=[z([t_1,t_1,t_1,t_2,t_2,t_3]);z([t_2,t_3,t_4,t_3,t_4,t_4])].';

H=sqrt((X(:,1)-X(:,2)).^2+(Y(:,1)-Y(:,2)).^2);%+(Z(:,1)-Z(:,2)).^2);
h=max(H);%,mean(H)];

